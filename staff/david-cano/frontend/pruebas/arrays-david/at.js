//El método at() recibe un valor numérico entero y devuelve el elemento en esa posición, permitiendo valores positivos y negativos. Los valores negativos contarán desde el último elemento del array.

// La función "at" toma un array y un índice como entrada y devuelve el valor en el índice especificado.
function at(array, index) {
    // Guardamos el valor del array en la posición "index" en una variable llamada "value".
    var value = array[index];

    // Devolvemos el valor almacenado en la variable "value".
    return value;
}


